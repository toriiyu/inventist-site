const HOLDERS = [["メーカー",0.50],["大学・研究機関",0.26],["スタートアップ・個人",0.24]];
function pickHolder(){const r=rnd();let a=0;for(const[h,p]of HOLDERS){a+=p;if(r<a)return h;}return "メーカー";}
