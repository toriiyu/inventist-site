function genTitle(f){ const v=FIELDS[f]; const mod = chance(0.7) ? pick(MODS)+" " : ""; return pick(v.pre)+" "+mod+pick(v.core)+pick(v.suf); }
function abstractOf(f,title){
  return `本サンプルは${f}分野の発明イメージです。${title.replace(/(の製造方法|の制御方法|およびその.*|を備える.*|とその.*)$/,'')}に関し、性能・歩留り・コストの少なくとも一つを改善する構成を示す（※実在の公報ではありません）。`;
}

